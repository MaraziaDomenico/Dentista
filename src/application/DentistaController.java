    public void aprifinestra( Rectangle r) {
    	if(r.getFill().equals(Color.RED)) {
    		try {

        		FXMLLoader fxmlLoader = new FXMLLoader(getClass().getResource("Gestione.fxml"));
        		Parent root2 = (Parent) fxmlLoader.load();
        		Stage stage = new Stage();
        		stage.setScene(new Scene(root2,720,400));  
        		stage.show();
        		GestioneController gc = fxmlLoader.getController();
        		gc.inizio(this, r);
        	} catch(Exception e) {
        		System.out.println(e.getMessage());
        	}
    		
    	
    		
    	}else {
    		r.setFill(Color.RED);
        	
        	try {

        		FXMLLoader fxmlLoader = new FXMLLoader(getClass().getResource("Prenotazione.fxml"));
        		Parent root1 = (Parent) fxmlLoader.load();
        		Stage stage = new Stage();
        		stage.setScene(new Scene(root1,475,600));  
        		stage.show();
        	} catch(Exception e) {
        		System.out.println(e.getMessage());
        	}
    	}
    }

